#!/usr/bin/env node
/**
 * Standalone Codex Skill Generator
 * Transforms Claude CLI commands to Codex CLI skills without VSCode dependencies
 */

import fs from 'fs';
import path from 'path';
import yaml from 'js-yaml';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const workspaceRoot = path.join(__dirname, '..');
const claudeCommandsDir = path.join(workspaceRoot, '.claude', 'commands');
const codexSkillsDir = path.join(workspaceRoot, '.system', 'skills');

// Pipeline sequence for auto-chain injection
const PIPELINE_SEQUENCE = [
  '0_business_scenario',
  '0a_problem_validation',
  '1_gofer_research',
  '2_gofer_specify',
  '3_gofer_plan',
  '4_gofer_tasks',
  '5_gofer_implement',
  '6_gofer_validate',
  '6a_gofer_engineering_review',
];

function getNextCommand(currentCommand) {
  const index = PIPELINE_SEQUENCE.indexOf(currentCommand);
  if (index >= 0 && index < PIPELINE_SEQUENCE.length - 1) {
    return PIPELINE_SEQUENCE[index + 1];
  }
  return null;
}

function extractYamlFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/);
  if (!match) {
    return { frontmatter: {}, body: content };
  }

  try {
    const frontmatter = yaml.load(match[1]);
    const body = match[2];
    return { frontmatter, body };
  } catch (error) {
    console.error('Failed to parse YAML frontmatter:', error.message);
    return { frontmatter: {}, body: content };
  }
}

function transformContent(content) {
  let transformed = content;

  // Remove Claude-specific AUTO-CHAIN sections
  transformed = transformed.replace(
    /\*\*AUTO-CHAIN[^]*?(?=\n##|\n---|\n\*\*|$)/g,
    ''
  );

  // Transform Skill tool invocations
  transformed = transformed.replace(
    /by calling the Skill tool with skill="([^"]+)"/g,
    'by running the next command'
  );
  transformed = transformed.replace(/Skill tool/g, 'next command');

  // Transform Task tool references for Codex (no Task tool support)
  transformed = transformed.replace(
    /Task: subagent_type="([^"]+)"/g,
    '**Note**: Codex CLI does not support the Task tool. For parallel agent work, open multiple Codex CLI sessions and run the $1 analysis in each.'
  );

  // Transform command invocation syntax: / → $ $
  // Handle numbered commands (including 6a style)
  transformed = transformed.replace(/\/(\d+[a-z]?_gofer_\w+)/g, (match, capture) => {
    return `$ $${capture}`;
  });
  // Handle non-numbered commands
  transformed = transformed.replace(/\/(gofer_\w+)/g, (match, capture) => {
    return `$ $${capture}`;
  });

  return transformed;
}

function injectPipelineContinuation(content, commandName) {
  const nextCommand = getNextCommand(commandName);

  if (!nextCommand) {
    return content; // No next command (end of pipeline or auxiliary command)
  }

  const continuationSection = `

## Pipeline Continuation

This completes the ${commandName} stage. To continue the Gofer pipeline:

**Next Command:** \`$ $${nextCommand}\`

The next stage will use the artifacts generated by this command and continue the implementation workflow.

**Note:** Codex CLI does not support automatic command chaining. You must manually run each stage command to progress through the pipeline.
`;

  // Inject before "Key Rules" section or at end
  if (content.includes('## Key Rules')) {
    return content.replace('## Key Rules', continuationSection + '\n## Key Rules');
  } else {
    return content + continuationSection;
  }
}

function generateCodexSkill(commandFile) {
  const commandName = path.basename(commandFile, '.md');
  const content = fs.readFileSync(commandFile, 'utf8');

  // Extract YAML frontmatter and body
  const { frontmatter, body } = extractYamlFrontmatter(content);

  // Transform content
  let transformedBody = transformContent(body);
  transformedBody = injectPipelineContinuation(transformedBody, commandName);

  // Build Codex YAML frontmatter
  const codexFrontmatter = {
    name: commandName,
    description: frontmatter.description || 'Gofer pipeline command',
    arguments: [
      {
        name: 'feature',
        description: 'Feature name or description',
        required: false,
      },
    ],
    result_schema: {
      type: 'object',
      properties: {
        output: {
          type: 'string',
          description: 'Path to generated artifact or execution summary',
        },
        status: {
          type: 'string',
          enum: ['success', 'error'],
        },
      },
    },
  };

  // Build complete skill file
  const skillContent =
    '---\n' + yaml.dump(codexFrontmatter) + '---\n\n' + transformedBody;

  // Write to .system/skills/{command-name}/SKILL.md
  const skillDir = path.join(codexSkillsDir, commandName);
  const skillPath = path.join(skillDir, 'SKILL.md');

  if (!fs.existsSync(skillDir)) {
    fs.mkdirSync(skillDir, { recursive: true });
  }

  fs.writeFileSync(skillPath, skillContent, 'utf8');

  return skillPath;
}

function main() {
  console.log('🚀 Codex Skill Generator');
  console.log('Feature: 028-cross-platform-command-parity\n');

  // Find all Claude command files
  const commandFiles = fs
    .readdirSync(claudeCommandsDir)
    .filter((file) => file.endsWith('.md'))
    .map((file) => path.join(claudeCommandsDir, file))
    .sort();

  console.log(`📋 Found ${commandFiles.length} Claude commands to transform\n`);

  const generatedPaths = [];

  for (const commandFile of commandFiles) {
    try {
      const skillPath = generateCodexSkill(commandFile);
      const relativePath = path.relative(workspaceRoot, skillPath);
      generatedPaths.push(relativePath);
      console.log(`✅ Generated: ${relativePath}`);
    } catch (error) {
      const commandName = path.basename(commandFile);
      console.error(`❌ Failed to generate ${commandName}:`, error.message);
    }
  }

  console.log(`\n✅ Generation complete! Generated ${generatedPaths.length} Codex skills`);
}

// Run if invoked directly
main();

export { generateCodexSkill, transformContent, injectPipelineContinuation };
